#abs : 절댓값
print(abs(-5))

#pow : n제곱 
print(pow(4, 2))

#max : 최댓값
print(max(5, 12))
print(max(5, 12, 32))

#min : 최솟값
print(min(5, 12))

#round : 반올림
print(round(3.14))
print(round(4.99))

from math import * #math 라이브러리 안의 모든 것을 쓰겠다

#floor : 내림
print(floor(4.99))

#ceil : 올림
print(ceil(3.14))

#sqrt : 제곱근
print(sqrt(16))